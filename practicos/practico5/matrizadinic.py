def mtog(m):
    result = {}
    for letra in range(len(m)):
        result["%s%s"%("0",chr(65+letra))] = 1
        for numero in range(len(m[letra])):
            result["%s%s"%(2+numero,"1")] = 1
            if m[letra][numero] == "1":
                result["%s%s"%(chr(65+letra),2+numero)] = 1
    return result


